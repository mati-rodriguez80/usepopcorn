import { useEffect, useState } from "react";

import StarRating from "./StarRating";

const average = (arr) => arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const OMDB_API_KEY = "6fc5422e";

export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");
  const [selectedMovieId, setSelectedMovieId] = useState(null);

  function handleSelectMovie(movieId) {
    setSelectedMovieId((currMovieId) => (movieId === currMovieId ? null : movieId));
  }

  function handleCloseMovie() {
    setSelectedMovieId(null);
  }

  useEffect(
    function () {
      async function fetchMovies() {
        try {
          setIsLoading(true);
          setError("");
          const res = await fetch(`http://www.omdbapi.com/?apikey=${OMDB_API_KEY}&s=${query}`);

          // Handle non-successful HTTP response
          if (!res.ok) throw new Error("Something went wrong with fetching movies");

          const data = await res.json();

          // Handle OMDB API errors (e.g., invalid query)
          if (data.Response === "False") throw new Error(data.Error);

          setMovies(data.Search);
        } catch (error) {
          // Check for network errors (TypeError)
          if (error instanceof TypeError) {
            setError("Network error: Failed to fetch movies. Please check your connection.");
          } else {
            setError(error.message); // Custom errors
          }
        } finally {
          setIsLoading(false);
        }
      }

      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }

      fetchMovies();
    },
    [query]
  );

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>

      <Main>
        <Box>
          {isLoading && <Loader />}
          {!isLoading && !error && <MoviesList movies={movies} onSelectMovie={handleSelectMovie} />}
          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          {selectedMovieId ? (
            <MovieDetail selectedMovieId={selectedMovieId} onCloseMovie={handleCloseMovie} />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList watched={watched} />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

